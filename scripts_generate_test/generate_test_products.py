def generate_product(index: int) -> dict:
    """Генерирует один товар в формате, похожем на JSON маркетплейса."""
    name, category, min_p, max_p = random.choice(PRODUCTS)
    price = random.randint(min_p, min(max_p, 1000))
    sku = str(uuid.uuid4())[:8].upper()
    product_id = f"WB{100000 + index}"
    url = f"{BASE_URL}/{product_id}"

    # Формат, похожий на реальный JSON маркетплейса (Ozon/WB-подобный)
    return {
        "id": product_id,
        "sku": sku,
        "name": name,
        "title": name,
        "description": random.choice(DESCRIPTIONS),
        "category": category,
        "offers": {
            "price": price,
            "priceCurrency": "RUB",
            "availability": "https://schema.org/InStock",
        },
        "url": url,
        "productUrl": url,
        "brand": {"name": "Willberis"},
        "image": f"https://willberis.ru/images/{product_id}.jpg",
    }


def main():
    out_dir = Path(__file__).parent
    out_dir.mkdir(exist_ok=True)

    # Генерируем 50 товаров
    products = [generate_product(i) for i in range(50)]

    # Сохраняем JSON (формат маркетплейса)
    json_path = out_dir / "products_marketplace.json"
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(products, f, ensure_ascii=False, indent=2)
    print(f"JSON: {json_path}")

    # Преобразуем в CSV (формат проекта)
    csv_path = out_dir / "products_test.csv"
    with open(csv_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=["name", "description", "category", "price", "marketplace_url"],
        )
        writer.writeheader()
        for p in products:
            writer.writerow(
                {
                    "name": p["name"],
                    "description": p.get("description", ""),
                    "category": p.get("category", ""),
                    "price": p["offers"]["price"],
                    "marketplace_url": p.get("url", p.get("productUrl", "")),
                }
            )
    print(f"CSV: {csv_path}")
    print("Создано 50 товаров, цены до 1000 руб, маркетплейс willberis")


if __name__ == "__main__":
    main()
